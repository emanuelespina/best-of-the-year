package org.bestoftheyear.best_of_the_year;

public class Song {

    private String id; 
    
    private String name;
   
    public Song (){}

    public void setId(String id) {
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }          

}